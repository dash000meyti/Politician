export const contentTypes = ["news", "video", "book", "event"]

export function getArticles({ type, limit, pinned } = {}) {
  let list = articles
  if (type) list = list.filter((a) => a.type === type)
  if (pinned !== undefined) list = list.filter((a) => a.pinned === pinned)
  list = [...list].sort(
    (a, b) => new Date(b.publishedAt) - new Date(a.publishedAt),
  )
  if (limit) list = list.slice(0, limit)
  return list.map((a) => normalizeArticle(a))
}

export function getArticleBySlug({ slug, type }) {
  const a = articles.find(
    (x) => x.slug === slug && (type ? x.type === type : true),
  )
  if (!a) return null
  return normalizeArticle(a)
}

export function normalizeArticle(article) {
  return {
    id: article.id,
    type: article.type,
    slug: article.slug,
    cover: article.cover,
    publishedAt: article.publishedAt,
    pinned: article.pinned,
    tags: article.tags,
    duration: article.duration,
    videoUrl: article.videoUrl,
    aparatHash: article.aparatHash,
    aparatEmbedUrl: article.aparatEmbedUrl,
    aparatScriptSrc: article.aparatScriptSrc,
    aparatRndDiv: article.aparatRndDiv,
    pages: article.pages,
    publisher: article.publisher,
    buyUrl: article.buyUrl,
    eventDate: article.eventDate,
    eventTime: article.eventTime,
    venue: article.venue,
    registerUrl: article.registerUrl,
    title: article.title,
    excerpt: article.excerpt,
    body: article.body,
    author: article.author,
  }
}
